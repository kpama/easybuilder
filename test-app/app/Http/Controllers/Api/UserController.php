     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $data = $request->all();
        $data['id'] = $id;

        $manipulator = new Manipulator();
        return $manipulator->createOrUpdate(User::class, $data);
    }

